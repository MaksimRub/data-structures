#include "AVL tree/AVL.h"
#include <stdio.h>
#include <stdlib.h>

// Вспомогательная функция для создания данных
Data* make_data(int key) {
    Data* d = (Data*)malloc(sizeof(Data));
    if (!d) {
        printf("Ошибка памяти\n");
        exit(1);
    }
    d->key = key;
    return d;
}

// Красивый визуализатор дерева на боку
void print_tree_2d(Node_avl* root, int space) {
    // Базовый случай
    if (root == NULL) return;

    // Увеличиваем расстояние между уровнями
    space += 6;

    // Сначала печатаем правое поддерево (оно будет вверху)
    print_tree_2d(root->right, space);

    // Печатаем текущий узел после нужного количества отступов
    printf("\n");
    for (int i = 6; i < space; i++) {
        printf(" ");
    }
    // Выводим ключ и его текущую высоту в дереве для контроля АВЛ
    printf("[%d:h=%d]\n", root->data->key, root->height);

    // Печатаем левое поддерево (оно будет внизу)
    print_tree_2d(root->left, space);
}

int main() {
    Node_avl* root = NULL;
    Data target;

    printf("=========================================\n");
    printf("1. ТЕСТ: Массовая вставка и автобалансировка\n");
    printf("=========================================\n");
    
    // Специальный набор ключей, который на обычном BST ушел бы в длинную ветку,
    // но в АВЛ обязан построить красивое, ветвистое дерево.
    int keys[] = {10, 20, 30, 40, 50, 25, 35, 27, 37};
    int n = sizeof(keys) / sizeof(keys[0]);

    for (int i = 0; i < n; i++) {
        printf("Вставляем: %d\n", keys[i]);
        insert(&root, make_data(keys[i]));
    }

    printf("\nВизуализация дерева на боку (с высотами узлов):\n");
    print_tree_2d(root, 0);
    printf("-----------------------------------------\n\n");

    printf("=========================================\n");
    printf("2. ТЕСТ: Функция поиска (search)\n");
    printf("=========================================\n");
    int search_keys[] = {27, 99}; // Один есть в дереве, второго нет
    for (int i = 0; i < 2; i++) {
        target.key = search_keys[i];
        Node_avl* found = search(root, &target);
        if (found) {
            printf("Узел %d НАЙДЕН. Высота узла в памяти: %d\n", found->data->key, found->height);
        } else {
            printf("Узел %d НЕ НАЙДЕН (как и должно быть).\n", target.key);
        }
    }
    printf("-----------------------------------------\n\n");

    printf("=========================================\n");
    printf("3. ТЕСТ: Удаление узла с перестройкой (del)\n");
    printf("=========================================\n");
    
    // Удалим узел 40. Это корень одного из поддеревьев, у него два ребенка (35 и 45).
    // Функция del должна вырезать преемника (45) и поставить на место 40.
    printf("Удаляем узел 40 (узел с двумя детьми)...\n");
    target.key = 40;
    del(&root, &target);

    printf("\nДерево после удаления 40:\n");
    print_tree_2d(root, 0);
    printf("-----------------------------------------\n\n");

    printf("=========================================\n");
    printf("4. ТЕСТ: Полная очистка памяти (free_tree)\n");
    printf("=========================================\n");
    free_tree(&root);
    
    if (root == NULL) {
        printf("Успех: Дерево полностью очищено, корень занулен!\n");
    } else {
        printf("Ошибка: Указатель на корень не обнулился.\n");
    }
    printf("=========================================\n");

    return 0;
}